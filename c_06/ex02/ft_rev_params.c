#include <unistd.h>

int	main(int nb, char **args)
{
	int	j;

	j = 0;
	while (nb > 1)
	{
		j = 0;
		while (args[nb - 1][j])
		{
			write(1, &args[nb - 1][j], 1);
			j++;
		}
		write(1, "\n", 1);
		nb--;
	}
}
